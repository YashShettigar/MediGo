    private void genOTP(){
        Pattern pattern = Pattern.compile("^(.+)@(.+)$");
        Matcher matcher = pattern.matcher(mailText.getText());
        if(!matcher.matches()){
                mailText.setText("Invalid Email !");
                mailText.setForeground(Color.RED);
        } else {
            timeOverClock.setText("00:40");
            Random r = new Random();
            number = r.nextInt(999999);
            Codewind.Send.mail("demodanger8@gmail.com", mailText.getText(), "1234567890@0", "OTP from MediGo", "Your OTP is "+Integer.toString(number));  
        }
    }
    
    
    Timer timer = new Timer();
    private void sendOtpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_sendOtpMouseClicked
        // TODO add your handling code here:
        genOTP();   //insatntiate Timer class
        timer.scheduleAtFixedRate(
                new java.util.TimerTask(){
                    public void run(){
                        String strTimer = timeOverClock.getText();
                        if(strTimer.equals("00:00")){
                            number=0;
                            timeOverClock.setText("");
                            OTP_ExpiredDialogBox OEDB = new OTP_ExpiredDialogBox();
                            OEDB.setVisible(true);
                            timer.cancel();
                        } else {
                            int sec = Integer.valueOf(strTimer.split(":")[1])-1; 
                            timeOverClock.setText(String.join(":", strTimer.split(":")[0], sec<10 ? "0"+String.valueOf(sec) : String.valueOf(sec)));
                        }
                    }
                },
                0, //delay
                1000  //repeat after 1s
        );
    }//GEN-LAST:event_sendOtpMouseClicked


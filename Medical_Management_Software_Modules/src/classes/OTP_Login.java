    private void loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginMouseClicked
        // TODO add your handling code here:
        if(OTP_text.getText().equals(Integer.toString(number))){
            timer.cancel(); //Clases Timer class's instance named timer
            String userID = "";
            try{
                
                String url = "jdbc:mysql://localhost:3306/medical_system?autoReconnect=true&useSSL=false";
                Class.forName("com.mysql.jdbc.Driver");
                Connection conn = DriverManager.getConnection(url, "root", "");
                Statement st = conn.createStatement();
                
                ResultSet rs = st.executeQuery("Select `User ID` from user_table where Email='"+mailText.getText()+"'");
                if(rs.next()){
                    userID = rs.getString("User ID");
                }
  
                conn.close();
            } catch (Exception e){
                System.out.println(e);
            }
            dispose();
            //Popup on successful login
            new Home(userID).setVisible(true);
            AccountVerifiedDialogBox AD = new AccountVerifiedDialogBox();
            AD.setVisible(true);
        } else {
            Invalid_OTP io = new Invalid_OTP();
            io.setVisible(true);
        }
    }//GEN-LAST:event_loginMouseClicked

